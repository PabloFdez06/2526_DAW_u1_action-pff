package org.example;

/**
 * Clase que representa un saludo.
 */
public class Saludo {

    /**
     * Devuelve un mensaje de saludo personalizado.
     *
     * @param nombre Nombre de la persona a saludar.
     * @return Mensaje de saludo.
     */
    public String saludar(String nombre) {
        return "Hola, " + nombre + "!";
    }
}
